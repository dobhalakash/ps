import { Injectable, signal } from '@angular/core';

export type Lang = 'en' | 'hi';

const LANG_KEY = 'th_lang';

/**
 * Lightweight runtime translation service (no external dependency).
 *
 * Usage in templates (import TPipe in the component):
 *   {{ 'nav.home' | t }}
 * Usage in TypeScript:
 *   this.i18n.t('nav.home')
 *
 * The chosen language persists in localStorage and applies instantly across
 * the app (signal-based). Missing Hindi keys automatically fall back to
 * English, so partially translated pages never show blank text.
 *
 * To translate more pages: add a key to BOTH dictionaries below and replace
 * the hard-coded string in the template with {{ 'your.key' | t }}.
 */
@Injectable({ providedIn: 'root' })
export class I18nService {

  /** Current UI language. */
  readonly lang = signal<Lang>(this.readStoredLang());

  setLang(lang: Lang): void {
    this.lang.set(lang);
    localStorage.setItem(LANG_KEY, lang);
    document.documentElement.lang = lang;
  }

  toggle(): void {
    this.setLang(this.lang() === 'en' ? 'hi' : 'en');
  }

  /** Translates a key in the current language, falling back to English. */
  t(key: string): string {
    const dict = this.lang() === 'hi' ? HI : EN;
    return dict[key] ?? EN[key] ?? key;
  }

  private readStoredLang(): Lang {
    const stored = localStorage.getItem(LANG_KEY);
    return stored === 'hi' ? 'hi' : 'en';
  }
}

